package EstruturasRepeticao;

public class EstruturaRepeticao04 {
    public static void main(String[] args) {

        /*Estrutura - FOR*/
        // A estrutura for é usada para repetir um bloco de código um número especifico de vezes

        int cont = 0;

        for (; cont<5;cont++){
            System.out.println("valor : " + cont);
        }
    }
}

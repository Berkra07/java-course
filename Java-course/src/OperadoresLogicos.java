public class OperadoresLogicos {
    public static void main(String[] args) {

        /* Operadores Lógicos
        *
        * && : e lógico (AND)
        * || : ou lógico (OR)
        * !: Negação lógica (NOT)*/


        boolean x = true;
        boolean y = false;

        System.out.println(x && y);
        System.out.println( x || y);
        System.out.println( !x);
    }
}
